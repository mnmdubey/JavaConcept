public class DataType {

	public static void main(String[] args) {
	//Integer - This is Integer from -Infinity to +Infinity
		int i = 10;
		int i1 = 20;
		int i2 = 100;
		int p = 11234;
		
		System.out.println(i);
		System.out.println(i2);
		System.out.println("This is Integer: "+p);
		
		System.out.println("+++++++++++++++++++++++++++");
		//Double - This is Decimal Number
		
		double d = 10;
		double d1 = 15.01;
		double d2 = 156.99;
		double d3 = 10125.88;
		double d14 = 14012.0;
		
		System.out.println(d);
		System.out.println(d3);
		System.out.println("Double (Decimal Number) is: "+d14);
		
		System.out.println("+++++++++++++++++++++++++++");
		//String - This is String, It should be in ""
		String fName = "Manoranjan";
		String lName = "Dubey";
		
		System.out.println("First Name: "+fName);
		System.out.println("Last Name: "+lName);
		
		System.out.println("Full Name: "+fName+" "+lName);
		
		System.out.println("====THE END, Thank you====");
	}
}
